const TABLE: Record<Lang, Record<TKey, string>> = { en: EN, ko: KO, zh: ZH, ja: JA }

/** Translate `key` for `lang`, filling `{placeholder}` params; falls back to English. */
export function t(lang: Lang, key: TKey, params?: Record<string, string | number>): string {
  let s = (TABLE[lang] ?? EN)[key] ?? EN[key]
  if (params) for (const [k, v] of Object.entries(params)) s = s.replaceAll(`{${k}}`, String(v))
  return s
}
